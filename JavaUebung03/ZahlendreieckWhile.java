 public class ZahlendreieckWhile {
	public static void main(String[] args){
	
	int zaehler = 0;
	String ausgabe = "";

	while (zaehler < 10){
		ausgabe = ausgabe + zaehler;
		System.out.println(ausgabe);
		zaehler ++;
		}
		}
} 